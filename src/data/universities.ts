export function searchUniversities(query: string): University[] {
  const lowerQuery = query.toLowerCase();
  return universities
    .filter(
      (u) =>
        u.name.toLowerCase().includes(lowerQuery) ||
        u.state.toLowerCase().includes(lowerQuery)
    )
    .slice(0, 20);
}
